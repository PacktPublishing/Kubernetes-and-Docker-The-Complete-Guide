#!/bin/bash
clear

tput setaf 5
echo -e "\n*******************************************************************************************************************"
echo -e "Installing KinD v0.8.0"
echo -e "*******************************************************************************************************************"
tput setaf 2
wget https://github.com/kubernetes-sigs/kind/releases/download/v0.8.0/kind-linux-amd64
curl -Lo ./kind https://kind.sigs.k8s.io/dl/v0.8.0/kind-linux-amd64
chmod +x ./kind

tput setaf 5
echo -e "\n*******************************************************************************************************************"
echo -e "Moving the KinD Binary to /usr/bin"
echo -e "*******************************************************************************************************************"
tput setaf 2
sudo mv ./kind /usr/bin

tput setaf 3
echo -e "\n*******************************************************************************************************************"
echo -e "KinD installation complete"
echo -e "*******************************************************************************************************************"
tput setaf 2
